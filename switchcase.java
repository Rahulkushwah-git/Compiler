import java.util.*;

public class switchcase {
    public static void main(String arg[]){
        Scanner sc=new Scanner(System.in);
        int ch=sc.nextInt();
        switch(ch){
            case 1: System.out.println("heloo");
            break;
            case 2:System.out.println("ram ram");
            break;
            case 3:System.out.println("Radhe Radhe");
            break;
            case 4:System.out.println("jai shree Ram");
            break;
            case 5:System.out.println("Jai jinendra");
            break;
            case 6:System.out.println("jai bhavani");
            break;
            default :System.out.println("Jai Hind");
        }
    }
}
